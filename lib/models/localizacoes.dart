class Localizacoes {
  String nome;
  String endereco;
  String foto;
  double latitude;
  double longitude;
  final String? descricao;

  Localizacoes({
    required this.nome,
    required this.endereco,
    required this.foto,
    required this.latitude,
    required this.longitude,
    required this.descricao,
  });
}
